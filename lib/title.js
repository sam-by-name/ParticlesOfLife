export const flicker = (x, count) => {
  for (let i = 0; i < x.length; i++) {
    for (let j = 0; j < x[i].length; j++) {
      if (count > (title.length * title[0].length) / 2) {
        x[i][j] = Math.round(Math.random())
      } else {        
        if (x[i][j]) x[i][j] = 0
        else x[i][j] = 1
      }
    }
  }
  return x
}
export const flicker = (x) => {
  loop1:
    for (let i = 0; i < x.length; i++) {
      for (let j = 0; j < x[i].length; j++) {
        if (title[i][j] && !x[i][j]) {
          x[i][j] = 1
          break loop1
        }
      }
    }
  loop2:
    for (let i = x.length - 1; i >= 0; i--) {
      for (let j = x[0].length - 1; j >= 0; j--) {
        if (title[i][j] && !x[i][j]) {
          x[i][j] = 1
          break loop2
        }
      }
    }
  return x
}

